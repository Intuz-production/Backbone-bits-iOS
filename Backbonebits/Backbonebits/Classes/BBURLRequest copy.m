@implementation BBURLRequest

+ (NSMutableURLRequest *)requestWithUrl:(NSString *)strUrl parameters:(NSDictionary *)parameters {
    NSURL * url = [NSURL URLWithString:strUrl];
    NSMutableURLRequest * urlRequest = [NSMutableURLRequest requestWithURL:url cachePolicy:NSURLRequestReloadIgnoringLocalCacheData timeoutInterval:30.0];
    [urlRequest setValue:@"application/x-www-form-urlencoded" forHTTPHeaderField:@"Content-type"];
    [urlRequest setHTTPMethod:@"POST"];
    if(parameters) {
        NSString *query = BBQueryStringFromParameters(parameters);
        [urlRequest setHTTPBody:[query dataUsingEncoding:NSUTF8StringEncoding]];
    }
    return urlRequest;
}

+ (NSMutableURLRequest *)multiPartRequestWithUrl:(NSString *)strUrl parameters:(NSDictionary *)parameters files:(NSArray *)files {
    NSURL * url = [NSURL URLWithString:strUrl];
    NSMutableURLRequest * urlRequest = [NSMutableURLRequest requestWithURL:url];
    [urlRequest setValue:@"application/x-www-form-urlencoded" forHTTPHeaderField:@"Content-type"];
    [urlRequest setHTTPMethod:@"POST"];
    BBStreamingMultipartFormData *formData = [[BBStreamingMultipartFormData alloc] initWithURLRequest:urlRequest stringEncoding:NSUTF8StringEncoding];
    if (parameters) {
        for (BBQueryStringPair *pair in BBQueryStringPairsFromDictionary(parameters)) {
            NSData *data = nil;
            if ([pair.value isKindOfClass:[NSData class]]) {
                data = pair.value;
            } else if ([pair.value isEqual:[NSNull null]]) {
                data = [NSData data];
            } else {
                data = [[pair.value description] dataUsingEncoding:NSUTF8StringEncoding];
            }
            
            if (data) {
                [formData appendPartWithFormData:data name:[pair.field description]];
            }
        }
    }

    [files enumerateObjectsUsingBlock:^(id obj, NSUInteger idx, BOOL *stop) {
        NSString *name = @"attachments[]";
        NSString *fileName = [obj lastPathComponent];
        NSString *mimeType = BBContentTypeForPathExtension([obj pathExtension]);
        [formData appendPartWithFileURL:obj name:name fileName:fileName mimeType:mimeType error:nil];
    }];
 
    return [formData requestByFinalizingMultipartFormData];
}

